int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 1)
		return (1);
	while (i <= nb / 2)
	{
		if (nb != (i * i))
		{
			i++;
		}
		else if (nb == (i * i))
		{
			return (i);
		}
	}
	return (0);
}
