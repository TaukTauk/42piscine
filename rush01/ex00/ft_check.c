int	ft_check(int grid[4][4], int index, int n)
{
	int	i;

	i = -1;
	while (++i < index / 4)
	{
		if (grid[i][index % 4] == n)
			return (1);
	}
	i = -1;
	while (++i < index % 4)
	{
		if (grid[index / 4][i] == n)
			return (1);
	}
	return (0);
}
